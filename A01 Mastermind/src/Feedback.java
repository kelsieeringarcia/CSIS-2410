package mastermind;

/**
 * Feedback is equivalent to the White and Black pegs used in the board game
 * version of Mastermind. A White Feedback means the Player guessed one Code
 * correctly, but in the wrong place, and a Black Feedback means the Player 
 * guessed correctly and in the correct place.
 * 
 * @author czuniga10
 *
 */
public enum Feedback {
	BLACK, WHITE;
}
